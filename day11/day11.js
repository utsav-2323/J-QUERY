$(document).ready(function(){
    $(".mt-img1").mouseenter(function(){
        $(".mt-tx1").addClass("line2");
        $(".m-im").addClass("mt-img1 img:hover");
        
    })
    $(".mt-img1").mouseleave(function(){
        $(".mt-tx1").removeClass("line2");
    })
})

